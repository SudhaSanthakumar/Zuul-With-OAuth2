package com.demo.util.model;


public class Car {
	
	private int id;
	
	private String name;

	public Car() {
		super();
	}

	public Car(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	

}
